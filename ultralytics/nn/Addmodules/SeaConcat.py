import torch
import torch.nn as nn
import math


class DeepSeaCenterConcat(nn.Module):
    def __init__(self, dimension=1):
        super(DeepSeaCenterConcat, self).__init__()
        self.dimension = dimension

    def forward(self, x):
        # ??????????
        if not isinstance(x, list):
            x = [x]

        # ?????????????
        if len(x) == 1:
            return x[0]

        # ???????????
        centers = []
        for feat in x:
            # ???????
            batch_size, channels, height, width = feat.size()

            # ???????
            center_h, center_w = height // 2, width // 2

            # ???????
            centers.append((center_h, center_w))

        # ?????????
        max_height = max([feat.size(2) for feat in x])
        max_width = max([feat.size(3) for feat in x])

        # ???????
        out_channels = sum([feat.size(1) for feat in x])
        out = torch.zeros(x[0].size(0), out_channels, max_height, max_width).to(x[0].device)

        # ??????????
        offset = 0
        for i, feat in enumerate(x):
            batch_size, channels, height, width = feat.size()
            center_h, center_w = centers[i]

            # ?????????????????
            start_h = max_height // 2 - center_h
            end_h = start_h + height
            start_w = max_width // 2 - center_w
            end_w = start_w + width

            # ???????? - ??????????????????????????
            depth_factor = self._get_deep_sea_weight(feat, center_h, center_w)

            # ????????????
            out[:, offset:offset + channels, start_h:end_h, start_w:end_w] = feat * depth_factor

            # ??????
            offset += channels

        return out

    def _get_deep_sea_weight(self, feat, center_h, center_w):
        """??????????????????????????????????"""
        batch_size, channels, height, width = feat.size()

        # ??????
        y_indices = torch.arange(height).view(-1, 1).expand(height, width).to(feat.device)
        x_indices = torch.arange(width).view(1, -1).expand(height, width).to(feat.device)

        # ?????????
        distance = torch.sqrt(
            (y_indices - center_h) ** 2 +
            (x_indices - center_w) ** 2
        )

        # ????????????? - ????????
        # ??????????????????????????
        max_distance = math.sqrt(center_h ** 2 + center_w ** 2)
        depth_effect = torch.exp(-1.5 * distance / max_distance)  # ??????

        # ?????? - ?????????????
        low_freq_boost = 1.0 + 0.5 * (1.0 - depth_effect)

        # ????????????????
        weight = depth_effect.unsqueeze(0).unsqueeze(0) * low_freq_boost.unsqueeze(0).unsqueeze(0)

        return weight